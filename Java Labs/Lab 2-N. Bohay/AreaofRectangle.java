public class AreaofRectangle
{
public static void main(String [] args)
{
    int w = 6 ;
    int  l = 8;
    
    //perimeter = 2*(l+w)
    int p = 2*(l+w);
    
    //area = l*w
    int a = w*l;
    
    System.out.println("the area is " +a+ " the perimeter is " + p);
}
}